package bank;
//Modulus for an angle by 360. Similar to Remainder afer division
//Rotation in degrees after completing one circle.
public class Modulus1 {

	public static void main(String[] args) {
		int angle1=65;
		int angle2=365;
		double angle3=725.50;
		System.out.println("Modulus of angle1: "+angle1%360);
		System.out.println("Modulus of angle2: "+angle2%360);
		System.out.println("Modulus of angle3: "+angle3%360);

	}

}
